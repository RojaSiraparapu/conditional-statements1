package condtional;
public class Arithmetic{
public static void main(String [] args) {
	int a=10;
	int b=15;
	char op='*';
	switch(op)
	{
		case '+':
	      System.out.println("Result:" +(a+b));
	        break;
	    case '-':
	    	 System.out.println("Result:"+(a-b));
	    break;
	    case '*':
	    	 System.out.println("Result:"+(a*b));
	    break;
	    case '/':
	    	 System.out.println("Result:"+(a/b));
	    break;
	    default:
	    	System.out.println("Invalid operator");
	          	}
	          	
}	    	
	    	    
	    
	    	    
	    	    
	    	
	              
			
	
	
	
	
}

