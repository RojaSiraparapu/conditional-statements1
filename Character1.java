public class Character1{

	public static void main(String[] args)
	{
		 char ch ='A';
		 if(Character.isLetter(ch))
		 {
			 System.out.println("letter");
		 }
		 else if(Character.isDigit(ch))
		 {
			 System.out.println("digit");
		 }
			 else{
				 System.out.println("special character");
				 
			 
			 
		 }
			 
	}
		
	}

